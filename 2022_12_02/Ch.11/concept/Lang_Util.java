/*	2. java.lang과 java.util 패키지
 * 
 * => java.lang 패키지
 * 	(1) Object 
 * 		- 자바 클래스의 최상위 클래스로 사용
 * 
 *  (2) System
 *  	- 표준 입력 장치(ex: 키보드)로부터 데이터를 입력받을 때 사용
 *  		-> 입력 장치를 추상화한 키워드: in, stdin
 *  			System.in <<
 *  			standard in
 *  
 *  	- 표준 출력 장치(ex: 모니터)로 출력하기 위해 사용
 *  		-> 출력 장치를 추상화한 키워드: out, stdout
 *  			System.out <<
 *  			standard out
 *  	
 *  	- 자바 가상 기계(JVM)를 종료시킬 때 사용
 *  	- 쓰레기 수집기(garbage collector)를 실행 요청할 때 사용
 *  		gc : garbage collector 메소드
 * 
 * 	(3) Class
 * 		- 클래스를 메모리로 로딩할 때 사용
 * 
 * 	(4) String
 * 		- 문자열을 저장하고 여러 가지 정보를 얻을 때 사용
 * 
 * 	(5) StringBuffer, StringBuilder
 * 		- 문자열을 저장하고 내부 문자열을 조작할 때 사용
 * 
 *  (6) Math 
 *  	- 수학 함수를 이용할 때 사용
 *  
 *  (7) Wrapper
 *  	-> Byte, Short, Character, Integer, Float, Double, Boolean, Long
 *  	
 *  	- 기본 타입의 데이터를 갖는 객체를 만들 때 사용
 *  	- 문자열을 기본 타입으로 변환할 때 사용
 *  	- 입력값 검사에 사용
 *  
 *  
 * => java.util 패키지
 * 		- 자바 프로그램 개발의 조미료 같은 역할
 * 	(1) Array
 * 		- 배열을 조작(비교, 복사, 정렬, 찾기)할 때 사용
 * 
 * 	(2) Calendar
 * 		- 운영체제의 날짜와 시간을 얻을 때 사용
 * 
 * 	(3) Date
 * 		- 날짜와 시간 정보를 저장하는 클래스
 * 
 * 	(4) Objects
 * 		- 객체 비교, 널(null) 여부 등을 조사할 때 사용
 * 
 * 	(5) StringTokenizer
 * 		- 특정 문자로 구분된 문자열을 뽑아낼 때 사용
 * 
 * 	(6) Random
 * 		- 난수를 얻을 때 사용
 * 
 */

