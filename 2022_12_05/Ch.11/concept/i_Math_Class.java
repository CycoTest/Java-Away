/*
 * 12. Math Class
 * 
 * 	(1) 절대값
 * 		-> 부호 여부 상관없이 무조건 양수로 리턴
 * 
 * 		a. int abs(int a)
 * 		b. double abs(double a)
 * 
 * 	(2) 올림값
 * 		-> 전달받은 값 중 가장 큰 정수로 리턴
 * 		
 * 		double ceil(double a)
 * 
 * 	(3) 버림값
 * 		-> 전달받은 값 중 가장 작은 정수로 리턴
 * 
 * 		double floor(double a)
 * 	
 * 	(4) 최대값
 * 		-> 두 수 중 가장 큰 값을 리턴
 * 
 * 		a. int max(int a, int b)
 * 		b. double max(double a, double b)
 * 
 * 	(5) 최소값
 * 		-> 두 수 중 가장 작은 값을 리턴
 * 	
 * 		a. int min(int a, int b)
 * 		b. double min(double a, double b)
 * 
 * 	(6) 난수 생성
 * 		-> 무작위의 수를 리턴
 * 
 * 		double random()
 * 
 * 	(7) 가까운 정수의 실수값
 * 		
 * 		double rint(double a)
 * 
 * 	(8) 반올림값
 * 		-> 5보다 같거나 크면 올림, 5보다 작으면 내림
 * 
 * 		long round(double a)
 */
